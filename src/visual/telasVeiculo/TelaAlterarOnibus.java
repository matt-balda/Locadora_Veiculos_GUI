    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String placa = null;
        int ano = 0, numPassageiros = 0;
        double valorDiaria = 0;
        Categoria categoria = null;
        boolean servicoInternet = false, arCondicionado = false;
        boolean dadosOnibusValido = false;

        try {
            placa = jTextPlaca.getText();
            ano = Integer.parseInt(jTextAno.getText());
            valorDiaria = Double.parseDouble(jTextValorDiaria.getText());
            numPassageiros = Integer.parseInt(jTextNumPass.getText());
        } catch (Exception exception) {

        }
        try {
            ExcecaoOnibus.dadosOnibusValido(placa, ano, valorDiaria, numPassageiros);
            dadosOnibusValido = true;
        } catch (Exception exception) {
            JOptionPane.showMessageDialog(null, exception.getMessage());
        }

        switch (jComboBoxCategoria.getSelectedItem().toString()) {
            case "Leito":
                categoria = Categoria.LEITO;
                break;
            case "Executivo":
                categoria = Categoria.EXECUTIVO;
                break;
            case "Convencional":
                categoria = Categoria.CONVENCIONAL;
                break;
        }

        servicoInternet = ExcecaoCarro.stringToBoolean(jComboBoxServicoInternet.getSelectedItem().toString());

        switch (jComboBoxArCondicionado.getSelectedItem().toString()) {
            case "Sim":
                arCondicionado = true;
                break;
            case "Não":
                arCondicionado = false;
                break;
        }

        if (dadosOnibusValido) {
                onibus.setPlaca(placa);
                onibus.setAno(ano);
                onibus.setValorDiaria(valorDiaria);
                onibus.setNumPassageiros(numPassageiros);
                onibus.setCategoria(categoria);
                onibus.setServicoInternet(servicoInternet);
                onibus.setArCondicionado(arCondicionado);
                
                System.out.println(onibus.toString());
                
                JOptionPane.showMessageDialog(null, "Placa: "+onibus.getPlaca()+
                    "\nAno: "+onibus.getAno()+
                    "\nValor da Diária: "+onibus.getValorDiaria()+
                    "\nNúmero de Passageiros: "+onibus.getNumPassageiros()+
                    "\nCategoria: "+onibus.getCategoria()+
                    "\nServiço de Internet: "+onibus.isServicoInternet()+
                    "\nAr-Condicionado: "+onibus.isArCondicionado()
                    , "Dados do Ônibus Alterado com sucesso!", JOptionPane.INFORMATION_MESSAGE);
                
                limparCampos();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

