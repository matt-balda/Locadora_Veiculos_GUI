    private void txtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPesquisarActionPerformed
        // TODO add your handling code here:
        String placaPesquisar;

        placaPesquisar = jTextPesquisaOnibus.getText();
        try {
            veiculo = GerenciadorVeiculos.INSTANCE.get(placaPesquisar);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        if (veiculo != null) {
            jTextPlaca.setText(veiculo.getPlaca());
            jTextAno.setText(Integer.toString(veiculo.getAno()));
            jTextValorDiaria.setText(Double.toString(veiculo.getValorDiaria()));
            if (veiculo instanceof Onibus) {
                Onibus onibus = (Onibus) veiculo;
                jTextNumPass.setText(Integer.toString(onibus.getNumPassageiros()));
                jComboBoxCategoria.setSelectedItem(onibus.getCategoria());
                jComboBoxServicoInternet.setSelectedItem(onibus.isServicoInternet());
                jComboBoxArCondicionado.setSelectedItem(onibus.isArCondicionado());
                this.onibus = onibus;
            }
        }
    }//GEN-LAST:event_txtPesquisarActionPerformed

