    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        // TODO add your handling code here:
        int codLocacao;

        codLocacao = Integer.parseInt(txtPesquisarExclusao.getText());
        System.out.println(txtPesquisarExclusao.getText());
        try {
            locacao = GerenciadorLocacoes.INSTANCE.get(codLocacao);
            JOptionPane.showMessageDialog(null, "Código: "+locacao.getCodLocacao()+"\nCliente: "+locacao.getCliente().getNome()+"\nVeículo: "+locacao.getVeiculo().getPlaca());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

        if (locacao != null) {
            try {
                int opt = JOptionPane.showConfirmDialog(null, "Você deseja excluir", "Excluir", JOptionPane.YES_NO_OPTION);
                GerenciadorLocacoes.INSTANCE.existe(codLocacao);
                GerenciadorLocacoes.INSTANCE.remove(codLocacao);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

