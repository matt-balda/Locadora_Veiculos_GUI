    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        // TODO add your handling code here:
        int codLocacao;

        codLocacao = Integer.parseInt(txtPesquisar.getText());
        System.out.println(txtPesquisar.getText());
        try {
            locacao = GerenciadorLocacoes.INSTANCE.get(codLocacao);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

        if (locacao != null) {
            jTextPesquisaCliente.setText(Long.toString(locacao.getCliente().getCPF()));
            jTextPesquisaVeiculo.setText(locacao.getVeiculo().getPlaca());
            jComboBoxSeguro.setSelectedItem(locacao.isSeguro());
            jTextValorDiario.setText(Double.toString(locacao.getValorDiaria()));
            jDateChooserDataInicial.setDate(locacao.getDataInicial());
            jDateChooserDataFinal.setDate(locacao.getDataFinal());
       }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

