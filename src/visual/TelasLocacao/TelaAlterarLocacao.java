    public void carregarClientes(){
           if (new File("clientes.bin").exists()){
              try {
                  GerenciadorClientes.INSTANCE = (GerenciadorClientes) Arquivo.ler("clientes.bin");
              }catch (Exception e){
                  System.err.println(e.getMessage());
              }
          }
    }
    
