    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String placa;
        long cpf;
        boolean seguro = false, dadosLocacaoValido = false;
        double valorDiaria = 0;
        Date dataInicial, dataFinal;
        
        cpf = Long.parseLong(jTextPesquisaCliente.getText());
        try {
            cliente = GerenciadorClientes.INSTANCE.get(cpf);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        placa = jTextPesquisaVeiculo.getText();
        try {
            veiculo = GerenciadorVeiculos.INSTANCE.get(placa);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        switch (jComboBoxSeguro.getSelectedItem().toString()) {
            case "Sim":
                seguro = true;
                break;
            case "Não":
                seguro = false;
                break;
        }
        try {
            valorDiaria = Double.parseDouble(jTextValorDiario.getText());
        } catch (Exception exception) {

        }
        try {
            ExcecaoLocacao.dadosLocacaoValido(valorDiaria);
            dadosLocacaoValido = true;
        } catch (Exception exception) {
            JOptionPane.showMessageDialog(null, exception.getMessage());
        }
        dataInicial = jDateChooserDataInicial.getDate();
        dataFinal = jDateChooserDataFinal.getDate();

        if (dadosLocacaoValido) {
            locacao.setCliente(cliente);
            locacao.setVeiculo(veiculo);
            locacao.setSeguro(seguro);
            locacao.setValorDiaria(valorDiaria);
            locacao.setDataInicial(dataInicial);
            locacao.setDataFinal(dataFinal);
            
            JOptionPane.showMessageDialog(null, locacao.toString());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

